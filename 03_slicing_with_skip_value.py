# slicing with skip value 

name = "shaikh faizan is great python learner till now he will continue thi journey"
print(name[2:10:3]) # here the skip value is 3 

name2 = "iamthemanthatwanttolearnandgrowinpythonprogramminglanguage"
#output would be "aikh f a" 
#the third value in the square brackets is the skip value which is used to skip characters in the string
#for example if the skip value is 2, it will take every second character from the string 
print(name2[2:18:2])